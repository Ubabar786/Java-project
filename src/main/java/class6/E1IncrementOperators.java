package class6;

public class E1IncrementOperators {
    public static void main(String[] args) {
        int age = 18;
        age++;   //variable is always increased by 1.
        System.out.println(age);

        int age2=18;
        //pre-increment first and then printing.   ++age;   //if you are performing more than one op in the same line.
        System.out.println(++age2);


    }
}
