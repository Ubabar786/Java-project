//Write a Java Program to check whether number is Even or Odd.
public class H2 {
    public static void main(String[] args) {
        int num = 10;

        if (num%10 == 0) {
            System.out.println("The number is even");

        }else {
            System.out.println("The number is odd");
        }
    }
}
