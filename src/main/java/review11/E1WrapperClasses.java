package review11;

public class E1WrapperClasses {
    public static void main(String[] args) {

        //Unboxed to boxing below

        int a=10;
        double b=a;
        System.out.println(b);

        int f=30;
        Integer g=f;
        System.out.println(g);

        double u=30.5;
        Double t=u;
        System.out.println(t);

        //Boxed to unboxing below

        Boolean o=false;
        boolean s=o;
        System.out.println(s);
    }
}
