package class2;

public class VariableValues {
    public static void main(String[] args) {

        byte numberBooks= 5;
        numberBooks= 8;
        System.out.println(numberBooks);

        short editions= 2;
        editions=3;
        System.out.println(editions);

        int originalEdition= 1;
        originalEdition=2;
        System.out.println(originalEdition);

        long pages= 5765675362342431L;
        pages=63458964547546L;
        System.out.println(pages);

        float copies= 541.478F;
        copies= 671.569F;
        System.out.println(copies);

        double printed= 698413.4082167;
        printed= 734653.253656;
        System.out.println(printed);

        char ageRating= 'G';
        ageRating= 'U';
        System.out.println(ageRating);

        boolean trueFalseBox= true;
        trueFalseBox= true;
        System.out.println(trueFalseBox);

    }
}
