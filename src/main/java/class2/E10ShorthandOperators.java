package class2;

public class E10ShorthandOperators {
    public static void main(String[] args) {

        int number = 10;
        number += 40;
        System.out.println(number);

        int number2 = 9;
        number2 *= 9;
        System.out.println(number2);

        int number3 = 18;
        number3 -= 8;
        System.out.println(number3);

        int number4 = 29;
        number4 /= 7;
        System.out.println(number4);

    }
}
